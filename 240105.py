class 行李:
    def __init__(self, 行李ID, 行李重量, 出發機場, 目的地機場, 旅客姓名):
        self.行李ID = 行李ID
        self.行李重量 = 行李重量
        self.出發機場 = 出發機場
        self.目的地機場 = 目的地機場
        self.行李所屬旅客姓名 = 旅客姓名

    def 查詢行李(self):
        return self  # 回傳整個行李物件

    def 列印行李資訊(self):
        print(f"行李ID: {self.行李ID}, 重量: {self.行李重量} 公斤, 出發機場: {self.出發機場}, 目的地機場: {self.目的地機場}, 旅客姓名: {self.行李所屬旅客姓名}")

    def 修改行李重量(self, 新重量):
        self.行李重量 = 新重量
        print(f"行李ID: {self.行李ID},更正行李重量為:{self.行李重量}公斤")

    def 修改出發機場(self, 新出發機場):
        self.出發機場 = 新出發機場
        print(f"行李ID: {self.行李ID},修改出發機場為:{self.出發機場}")

    def 修改目的地機場(self, 新目的地機場):
        self.目的地機場 = 新目的地機場
        print(f"行李ID: {self.行李ID},修改目的地機場為:{self.目的地機場}")

    def 修改旅客姓名(self, 新旅客姓名):
        self.行李所屬旅客姓名 = 新旅客姓名
        print(f"行李ID: {self.行李ID},修改旅客姓名為:{self.行李所屬旅客姓名}")


