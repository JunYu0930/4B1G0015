"""題目1"""
class 角色:
    def __init__(self, 姓名, 年齡, 性別):
        """建構子:初始化屬性"""
        self.姓名 = 姓名
        self.年齡 = 年齡
        self.性別 = 性別

        """self:區分實例的屬性和傳遞進來的參數"""

    def 印出角色資訊(self):
        print(f"姓名:{self.姓名},年齡:{self.年齡},性別:{self.性別}")

    def 慶祝生日(self):
        self.年齡 += 1
        print(f"{self.姓名}{self.年齡}歲生日快樂！")

    def 更改性別(self, 新性別):
        self.性別 = 新性別
        print(f"{self.姓名}性別已更改為{self.性別}。")

# 創建三個物件
人物1 = 角色("小王", 25, "女性")
人物2 = 角色("張三", 30, "男性")
人物3 = 角色("李四", 22, "男性")

人物1.印出角色資訊()
人物2.印出角色資訊()
人物3.印出角色資訊()

人物1.慶祝生日()
